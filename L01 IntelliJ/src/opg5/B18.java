package opg5;

public class B18 {
    public static void main(String[] args) {
        double pi = 3.14159;
        double radius = 6.5;

        double perimeter = 2 * radius * pi;
        double area = radius * radius * pi;

        System.out.println("Perimeter er = " + perimeter);
        System.out.println("Area er = " + area);
    }
}
